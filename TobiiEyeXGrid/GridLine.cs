namespace TobiiEyeXGrid
{
    public class GridLine
    {
        public float x1, x2;
        public float y1, y2;

        public GridLine(float newX1, float newY1, float newX2, float newY2) {
            x1 = newX1;
            y1 = newY1;
            x2 = newX2;
            y2 = newY2;
        }

    }
}
